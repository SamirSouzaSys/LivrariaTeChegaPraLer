package model;

public class Editora {

    private int idEditora;
    private String nomeEditora;

    public int getId() {
        return idEditora;
    }

    public String getNome() {
        return nomeEditora;
    }

    public void setId(int id) {
        idEditora = id;
    }

    public void setNome(String nome) {
        nomeEditora = nome;
    }
}